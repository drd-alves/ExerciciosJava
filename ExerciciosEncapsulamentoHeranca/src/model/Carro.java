package model;

public class Carro extends Veiculos{
	
	private int numPortas;
	private int numPasageiros;
	private String tamanhoPortaMalas;
	
	public int getNumPortas() {
		return numPortas;
	}
	public void setNumPortas(int numPortas) {
		this.numPortas = numPortas;
	}
	public int getNumPasageiros() {
		return numPasageiros;
	}
	public void setNumPasageiros(int numPasageiros) {
		this.numPasageiros = numPasageiros;
	}
	public String getTamanhoPortaMalas() {
		return tamanhoPortaMalas;
	}
	public void setTamanhoPortaMalas(String tamanhoPortaMalas) {
		this.tamanhoPortaMalas = tamanhoPortaMalas;
	}
	
	
	
}
